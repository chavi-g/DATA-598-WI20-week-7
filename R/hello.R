
#' Function to add two numbers
#'
#' @param a
#' @param b
#'
#' @return
#' @export
#'
#' @examples
#' add_two_numbers(1, 3)
#' add_two_numbers(-5, 6)
add_two_numbers <- function(a, b) {
  return(a+b)
}


#' Function to multiply two numbers
#'
#' @param a
#' @param b
#'
#' @return
#' @export
#'
#' @examples
#' multiply_two_numbers(4, 5)
#' multiply_two_numbers(6, -3)
multiply_two_numbers <- function(a, b) {
  return(a*b)
}
